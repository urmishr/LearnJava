package Loops;

public class WhileLoop {
	public static void main(String[] args) {
		
		int i = 0; // initializing loop with 0
		while (i < 11) { 
			System.out.println(i);
			i++;
			
			/* if value of i is less than 11
			   print i & increase value of i (for now i is 0) with 1 
			   go back to printing i which will be now 1 and again increment */
		}
	}
}
