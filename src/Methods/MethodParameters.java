package Methods;

public class MethodParameters {
	
	
	// We have created a method that takes a "Parameter".
	//Parameter is what's Inside a bracket.
	public static void userName(String uName) {
		System.out.println("Your user name is : " + uName);
	}

	public static void main(String[] args) {

		userName("Urmish"); //Giving our parameter a value  
		
		
	}

}
